package com.example.customgridviewwithcardview;

public class Gridviewdata {

    private int image;
    private String title;

    public Gridviewdata(int image, String title){
        this.image = image;
        this.title = title;

    }

    public int getImage(){
        return image;
    }

    public String getTitle(){
        return title;
    }
}
